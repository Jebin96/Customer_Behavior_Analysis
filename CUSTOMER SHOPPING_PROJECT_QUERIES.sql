7. Segment Customers into new, Returning and loyal based on their total number of previous purchases
   and show the count of each segment

with customer_type as (
select customer_id,previous_purchases,
   case when previous_purchases = 1 then 'New'
        when previous_purchases between 2 and 10 then 'Returning'
		else 'Loyal'
		end as customer_segment
from jebinraja
)
select customer_segment,count(*) as Count_Of_Each_Segment
from customer_type
group by customer_segment

8. What are the top 3 most purchased products within each category?

SOLUTIONS:

with item_count as(
select category,item_purchased,
count(customer_id) as total_orders,
row_number() over(partition by category order by count(customer_id) desc) as item_rn
from jebinraja
group by category,item_purchased
)
select item_rn,item_purchased,category,total_orders
from item_count
where item_rn <=3

9. Are customers who are repeat buyers (more than 5 previous purchases) also likely to subscribe?

select subscription_status,count(previous_purchases) as repeat_buyers
from jebinraja
where previous_purchases > 5
group by subscription_status

