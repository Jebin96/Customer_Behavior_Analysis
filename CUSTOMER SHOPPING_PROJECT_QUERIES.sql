1`. What is the total revenue generated by male vs female customers?

select gender,sum(purchase_amount) as total_revenue
from jebinraja
group by gender

2. Which customer used a discount but still spent more than the average purchase amount?

select customer_id,purchase_amount
from jebinraja
where discount_applied = 'Yes'
and purchase_amount >= (select avg(purchase_amount) from jebinraja)

3. which are the top 5 products with the highest average review rating? 

SELECT item_purchased as Products,round(AVG(review_rating)::numeric,2) as Average_Review_Rating
FROM jebinraja
GROUP BY products
ORDER BY average_review_rating desc
LIMIT 5

